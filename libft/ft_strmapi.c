#include "libft.h"
#include <stdlib.h>

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*dest;
	unsigned int	i;

	i = 0;
	if (!s || !(*f))
		return (NULL);
	dest = NULL;
	dest = (char *)ft_malloc
		(dest, (sizeof(*dest) * (ft_strlen((char *)s) + 1)));
	if (dest == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		dest[i] = (*f)(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
