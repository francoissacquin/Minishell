#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t len)
{
	unsigned int	i;

	i = 0;
	if (!dest && !src)
		return (NULL);
	if (dest > src)
		while (len--)
			((char *)dest)[len] = ((char *)src)[len];
	else
	{
		while (i < len)
		{
			((char *)dest)[i] = ((char *)src)[i];
			i++;
		}
	}
	return (dest);
}
